package co.in.nextgencoder.class7;

public class LongDemo {

	public static void main(String[] args) {

		// Long should be used for more 
		// longer ranged numbers
		
		long num1 = 1297409124;
		long num2 = 1234709472;
		
		long sum = num1 + num2;
		
		System.out.println( sum);	// -1762848700 if used only int for num1 and num2
		// 2532118596 after using long
		

	}

}
