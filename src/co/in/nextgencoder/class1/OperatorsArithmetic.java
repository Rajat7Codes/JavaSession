public class OperatorsArithmetic {
	public static void main(String[] args) {

		/*                            *
		 *      Arithmetic Operators  *
		 * 		-----------------	  *
		 *							  *
		*/
		
		// Precedence ( Which operates first )
		
		// 1. ( * ) Multiplication 			2. ( / ) Divide
		// 3. ( % ) Modulo					4. ( + ) Addition
		// 5. ( - ) Subtract
		

		// Addition
		System.out.println("( 9 + 2 ) = " + (9 + 2) );
		
		// Subtraction
		System.out.println("( 9 - 2 ) = " + (9 - 2));
		
		// Multiplication
		System.out.println("( 9 * 2 ) = " + (9 * 2));
		
		// Quotient after division
		System.out.println("( 9 / 2 ) = " + (9 / 2));
		
		// Reminder after division
		System.out.println("( 9 % 2 ) = " + (9 % 2));	
	}
}
